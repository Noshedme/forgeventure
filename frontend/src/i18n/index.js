// ── Core API ───────────────────────────────────────────────────────────────

export function getLang() {
  try { return localStorage.getItem("fv_lang") || "es"; }
  catch { return "es"; }
}

export function setLang(code) {
  try { localStorage.setItem("fv_lang", code); }
  catch {}
  window.dispatchEvent(new CustomEvent("fv-lang-changed", { detail: code }));
}

/** Translate a key. Falls back: requested lang → Spanish → raw key. */
export function t(key, lang) {
  const L = lang || getLang();
  return LOCALES[L]?.[key] ?? LOCALES.es?.[key] ?? key;
}
